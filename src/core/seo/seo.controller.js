'use strict';

const sitemapService = require('./services/sitemap.service');
const metaService = require('./services/meta.service');
const ApiResponse = require('../../api/v1/responses/api-response');

const getRobotsTxt = (req, res) => {
  const protocol = req.secure ? 'https' : 'http'; // Trust proxy must be true if behind nginx
  const host = req.get('host');
  const sitemapUrl = `${protocol}://${host}/sitemap.xml`;

  const robots = `User-agent: *\nAllow: /\n\nSitemap: ${sitemapUrl}`;
  res.type('text/plain');
  res.send(robots);
};

const getSitemapXml = async (req, res, next) => {
  try {
    const protocol = req.secure ? 'https' : 'http';
    const host = req.get('host');
    const baseUrl = `${protocol}://${host}`;
    
    const xml = await sitemapService.generateXml(baseUrl);
    res.type('application/xml');
    res.send(xml);
  } catch (error) {
    next(error);
  }
};

const getHeadlessMeta = (req, res) => {
  // Contoh API untuk Headless Frontend
  const { url, title, description } = req.query;
  const meta = metaService.getHeadlessMetadata({
    title: title || 'SIMAJU Framework',
    description: description || 'Generated by SIMAJU SEO Engine',
    url: url || 'https://simaju.com'
  });

  res.json(ApiResponse.success(meta, 'Metadata generated'));
};

module.exports = {
  getRobotsTxt,
  getSitemapXml,
  getHeadlessMeta
};
